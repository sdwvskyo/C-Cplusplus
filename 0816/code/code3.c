//3. 找出个位数为6能被3整除的5位数

#include<stdio.h>

int main(int argc, char *argv[])
{
	int i;
	for (i = 10006; i < 99999; i += 10) {
		if (i % 3 == 0) {
			printf("%d\t", i);
		}
	}
	printf("\n");
}

